#include "3-calc.h"

/**
 * op_mul - prints product.
 * @a: input int
 * @b: input int
 * Return: product.
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_add - prints sum of numbers.
 * @a: input int
 * @b: input int
 * Return: sum.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - prints difference.
 * @a: input int
 * @b: input int
 * Return: difference.
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mod - prints modulus.
 * @a: input int
 * @b: input int
 * Return: modulus.
 */

int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_div - prints quotient.
 * @a: input int
 * @b: input int
 * Return: quotient.
 */

int op_div(int a, int b)
{
	return (a / b);
}
